public class PhotoData {
    //"\"500\": [{w:0, h:0}, { w: 500, h: 375, src: 'http://multiply.com/mu/tela
    private Integer w;
    private Integer h;
    private String src;

    public Integer getW() {
        return w;
    }

    public void setW(Integer w) {
        this.w = w;
    }

    public Integer getH() {
        return h;
    }

    public void setH(Integer h) {
        this.h = h;
    }

    public String getSrc() {
        return src;
    }

    public void setSrc(String src) {
        this.src = src;
    }
    
}
